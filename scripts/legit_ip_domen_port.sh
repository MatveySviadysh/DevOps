#!/bin/bash

# –¶–≤–µ—Ç–∞
GREEN='\033[0;32m'
RED='\033[0;31m'
NC='\033[0m' # –ë–µ–∑ —Ü–≤–µ—Ç–∞

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤
if [ "$#" -lt 3 ]; then
  echo -e "${RED}Usage: $0 <domain> <ip_address> <port>${NC}"
  exit 1
fi

DOMAIN=$1
IP=$2
PORT=$3

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –¥–æ–º–µ–Ω–Ω–æ–≥–æ –∏–º–µ–Ω–∏
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –¥–æ–º–µ–Ω–∞: $DOMAIN"
ping -c 2 $DOMAIN > /dev/null 2>&1
if [ $? -eq 0 ]; then
  echo -e "${GREEN}‚úÖ –î–æ–º–µ–Ω $DOMAIN –¥–æ—Å—Ç—É–ø–µ–Ω${NC}"
else
  echo -e "${RED}‚ùå –î–æ–º–µ–Ω $DOMAIN –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω${NC}"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ IP-–∞–¥—Ä–µ—Å–∞
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ IP: $IP"
ping -c 2 $IP > /dev/null 2>&1
if [ $? -eq 0 ]; then
  echo -e "${GREEN}‚úÖ IP $IP –¥–æ—Å—Ç—É–ø–µ–Ω${NC}"
else
  echo -e "${RED}‚ùå IP $IP –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω${NC}"
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –æ—Ç–∫—Ä—ã—Ç–æ–≥–æ –ø–æ—Ä—Ç–∞
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ—Ä—Ç–∞ $PORT –Ω–∞ $IP"
nc -zv $IP $PORT > /dev/null 2>&1
if [ $? -eq 0 ]; then
  echo -e "${GREEN}‚úÖ –ü–æ—Ä—Ç $PORT –Ω–∞ $IP –æ—Ç–∫—Ä—ã—Ç${NC}"
else
  echo -e "${RED}‚ùå –ü–æ—Ä—Ç $PORT –Ω–∞ $IP –∑–∞–∫—Ä—ã—Ç –∏–ª–∏ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω${NC}"
fi
